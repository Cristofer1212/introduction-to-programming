package E4;

public class Condicional {
    public static void main(String[] args) {
        var numeroIf = 15;
        if (numeroIf > 0) {
            System.out.println("Es Positivaso");
        } else if (numeroIf < 0){
            System.out.println("Es Negativo");
        } else if (numeroIf == 0) {
            System.out.println("Es 0");
        }
    }
}


